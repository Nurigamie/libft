#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*str;

	str = (t_list *)malloc(sizeof(t_list));
	if (!str)
		return (0);
	str -> next = NULL;
	str -> content = content;
	return (str);
}
