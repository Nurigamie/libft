size_t strlcat(char *dest, const char *src, size_t size)
{
    int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != size)
	{
		dest[i] = src[j];
		i++;
		j++;
	}

}

